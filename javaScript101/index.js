//Using Comparison & Equality Operators
console.log(1 == 2) // This is false.
console.log(3 <= 4) // This is true.
console.log(5 != 6) // This is true.

//Single Equals: Assignment
var x = 3
console.log("x is", x) // This prints "x is 3".

//Double Equals: Abstract Equality Comparison
console.log(5 == "5") // This prints true.

//Triple Equals: Strict Equality Comparison
console.log(5 === "5") // This prints false.

//Comparisons With Booleans
console.log(true == true) // Yes
console.log(false == false) // Yes
console.log(false == true) // No

//Comparisons With Strings
console.log("a" == "b") // This is false.
console.log("a" <= "b") // This is true.
console.log("a" == "A") // This is false.
console.log("B" < "a") // This is true.

console.log("1" < "2") // This is true.
console.log("2" < "3") // This is true.
console.log("11" < "2") // What? This is true, too.



